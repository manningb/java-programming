package chapter2_examples;

public class ComputeArea2 {
	// main method
	public static void main(String[] args) {
		double radius = 20; // Declare and initialise radius
		double area = radius * radius * 3.14159; // Declare and compute area

		// Display results
		System.out.println("The area of the circle of radius " + 
				radius + " is " + area);
	}
}
